package review4;

public class Loops {
    public static void main(String[] args) {


        for(int i=2; i>10; i++);

        System.out.println("---------------------------------");

        for(int j=3; j<10; j--){
            System.out.println(j);

        }
        System.out.println("__________________");
        for(int i=5; i<=1; i--){
            System.out.println("Happy");

        }



    }
}
