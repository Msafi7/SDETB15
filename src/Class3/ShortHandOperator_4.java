public class ShortHandOperator_4 {
    public static void main(String[] args) {
        int number=10;
        number=number+10;
        number+=20; //whatever number is stored in number variable add 10 to it.
        System.out.println(number);
        number *=20;//whatever is stored in number variable *20

        int number2=20;
        number2-=10;
        System.out.println(number2);

        int number3=100;
        number3/=10;
        System.out.println(number3);






    }
}

