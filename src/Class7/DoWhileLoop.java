package Class7;

import java.util.Scanner;

public class DoWhileLoop {
    public static void main(String[] args) {

        Scanner scan=new Scanner(System.in);
        int i;

        do{
            System.out.println("Please Enter a number");
            i=scan.nextInt();
        }while(i!=5);

        System.out.println("******************888");



    }

}
