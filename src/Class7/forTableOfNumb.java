package Class7;public class forTableOfNumb {
    public static void main(String[] args) {

        //table of 5.

        for(int i=1; i<=10; i++){
            System.out.println("5 * "+i+" = " +(5*i));
        }

        System.out.println("*******************************************");
        //table of 6

        for(int j=1; j<=10; j++){
            System.out.println("6 * "+j+" = "+(6*j));
        }
    }
}
