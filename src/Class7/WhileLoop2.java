package Class7;

public class WhileLoop2 {
    public static void main(String[] args) {
        // Print the numbers from 0-9.

        int i=0;
        while(i<10){
            System.out.println(i);
            i++;

        }

        System.out.println("************************************");
        for(int j=0; j<10; j++){
            System.out.println(j);
        }
    }
}
