package Class3Package;

public class VariableDemo2 {
    public static void main(String[] args) {
        int age=10;
        System.out.println(age);
        //To re-assign value to the variable
        age=20;
        int number=10;
        age=number;
        System.out.println(number);
        age=number+10;
        System.out.println(age);
        age=age+20;
        System.out.println(age)
        ;
    }
}
